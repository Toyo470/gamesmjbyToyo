function get_card_path( card_value )
	-- body
	local card_type = math.modf(card_value / 100)
	local value = math.modf(card_value % 100)

	local tb = {
			[1] = "wan",
			[2] = "tong",
			[3] = "tiao",
			[4] = "zi"
	}
	if card_type == 1 or card_type == 2 or card_type == 3 or card_type == 4 then

		--print("card_type------value-",GAMEBASENAME,card_type,value)
		--print(GAMEBASENAME .. "/res/majiangCard/" ..tb[card_type] .. "/"..value..".png")
		return GAMEBASENAME .. "/res/majiangCard/" ..tb[card_type] .. "/"..value..".png"
	else
		return ""
	end
	
end